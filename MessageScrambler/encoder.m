        f = 8;
    elseif S == 'i'
        f = 9;   
    elseif S == 'j'
        f = 10;
    elseif S == 'k'
        f = 11;
    elseif S == 'l'
        f = 12;
    elseif S == 'm'
        f = 13;
    elseif S == 'n'
        f = 14;
    elseif S == 'o'
        f = 15;
    elseif S == 'p'
        f = 16;
    elseif S == 'q'
        f = 17;  
    elseif S == 'r'
        f = 18;
    elseif S == 's'
        f = 19;
    elseif S == 't'
        f = 20;   
    elseif S == 'u'
        f = 21;
    elseif S == 'v'
        f = 22;
    elseif S == 'w'
        f = 23;
    elseif S == 'x'
        f = 24;
    elseif S == 'y'
        f = 25;
