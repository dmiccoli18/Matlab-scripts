function L = freqFinder(f)
% find frequency values that are whole number integers of f
    if f == 5
        L = 'a';
    elseif f == 10
        L = 'b';
    elseif f == 15
        L = 'c';
    elseif f == 20
        L = 'd';
    elseif f == 25
        L = 'e';
    elseif f == 30
        L = 'f';
    elseif f == 35
        L = 'g';
    elseif f == 40
        L = 'h';
    elseif f == 45
        L = 'i';   
    elseif f == 50
        L = 'j';  
    elseif f == 55
        L = 'k';
    elseif f == 60
        L = 'l';
    elseif f == 65
        L = 'm';
    elseif f == 70
        L = 'n';
    elseif f == 75
        L = 'o';
    elseif f == 80
        L = 'p';
    elseif f == 85
        L = 'q'; 
    elseif f == 90
        L = 'r';
    elseif f == 95
        L = 's';
    elseif f == 100
        L = 't'; 
    elseif f == 105
        L = 'u';
    elseif f == 110
        L = 'v';
    elseif f == 115
        L = 'w';
    elseif f == 120
        L = 'x';
    elseif f == 125
        L = 'y';
    elseif f == 130
        L = 'z';
    elseif f == 135
        L = '.';
    elseif f == 140
        L = ';';
    elseif f == 145
        L = ',';
    elseif f == 180
        L = ' ';
    end