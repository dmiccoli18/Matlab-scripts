function f = letterFinder(S)
% find frequency values that are whole number integers of f
    if S == 'a'
        f = 5;
    elseif S == 'b'
        f = 10;
    elseif S == 'c'
        f = 15;
    elseif S == 'd'
        f = 20;
    elseif S == 'e'
        f = 25;
    elseif S == 'f'
        f = 30;
    elseif S == 'g'
        f = 35;
    elseif S == 'h'
        f = 40;
    elseif S == 'i'
        f = 45;   
    elseif S == 'j'
        f = 50;
    elseif S == 'k'
        f = 55;
    elseif S == 'l'
        f = 60;
    elseif S == 'm'
        f = 65;
    elseif S == 'n'
        f = 70;
    elseif S == 'o'
        f = 75;
    elseif S == 'p'
        f = 80;
    elseif S == 'q'
        f = 85;  
    elseif S == 'r'
        f = 90;
    elseif S == 's'
        f = 95;
    elseif S == 't'
        f = 100;   
    elseif S == 'u'
        f = 105;
    elseif S == 'v'
        f = 110;
    elseif S == 'w'
        f = 115;
    elseif S == 'x'
        f = 120;
    elseif S == 'y'
        f = 125;
    elseif S == 'z'
        f = 130;
    elseif S == '.'
        f = 135;
    elseif S == ';'
        f = 140;
